#include <stdio.h>

int main() {
    int n, sum;

    // Input n
    scanf("%d", &n);

    // Formula for sum of first n natural numbers
    sum = n * (n + 1) / 2;

    // Print result
    printf("Sum=%d\n", sum);

    return 0;
}
